export function ComplexNavbar() {
  const [isNavOpen, setIsNavOpen] = React.useState(false);
  const location = useLocation(); // ดึงข้อมูลเส้นทางปัจจุบัน

  // สร้าง breadcrumb จาก path
  const generateBreadcrumb = (path) => {
    const segments = path.split("/").filter(Boolean);
    return segments.map((segment, index) => (
      <span key={index}>
        {segment.charAt(0).toUpperCase() + segment.slice(1)}
        {index < segments.length - 1 && " / "}
      </span>
    ));
  };

  // const toggleIsNavOpen = () => setIsNavOpen((cur) => !cur);

  React.useEffect(() => {
    window.addEventListener(
      "resize",
      () => window.innerWidth >= 960 && setIsNavOpen(false)
    );
  }, []);

  return (
    <>
      {/* <Navbar className="mx-auto max-w-screen-xl p-2 lg:rounded-full lg:pl-6"> */}
      <Navbar className="hidden md:block mx-auto p-2 lg:rounded-full lg:pl-6">
        <div className="relative mx-auto flex justify-end items-center text-blue-gray-900">
          <div className="flex">
            <Button size="sm" variant="text">
              <BellIcon className="h-5 w-5 text-blue-gray-500" />
            </Button>

            <ProfileMenu />
          </div>
        </div>
        <Collapse open={isNavOpen} className="overflow-scroll">
          <NavList />
        </Collapse>
      </Navbar>
      <div className="md:mt-3 font-light">
        <a className="opacity-50">Pages / </a>
        <a>{generateBreadcrumb(location.pathname)}</a> {/* แสดง breadcrumb */}
        <p className="font-semibold">{generateBreadcrumb(location.pathname)}</p>
      </div>
    </>
  );
}
